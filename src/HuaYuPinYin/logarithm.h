static unsigned char GetBit(const unsigned char *const bit_array,
                            const size_t bit_index) {
  const size_t byte_index = bit_index >> 3;
  const size_t bit_position = 7 - (bit_index & 7);
  return (bit_array[byte_index] >> bit_position) & 1;
}
static void SetBit(unsigned char *const bit_array, const size_t bit_index) {
  const size_t byte_index = bit_index >> 3;
  const size_t bit_position = 7 - (bit_index & 7);
  bit_array[byte_index] |= 1 << bit_position;
}
